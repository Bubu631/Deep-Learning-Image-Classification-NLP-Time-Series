import numpy as np

class MovingAverage:
    def __init__(self, q):
        self.q = q

    def fit_transform(self, x):
        x = np.asarray(x, dtype=float)
        n = len(x)
        predictions = []

        for i in range(n):
            start = max(0, i - self.q + 1)
            window = x[start : i + 1]
            predictions.append(window.mean())

        return np.array(predictions)

